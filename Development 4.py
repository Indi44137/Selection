#Programme displaying your exam grade

exam_grade = int(input("Please enter your exam mark out of 100: "))

if exam_grade < 40:
    print("Your exam grade is a U")
elif exam_grade <= 50 >= 41:
    print("Your exam grade is a E")
elif exam_grade <= 60 >= 51:
    print("Your exam grade is a D")
elif exam_grade <= 70 >= 61:
    print("Your exam grade is a C")
elif exam_grade <= 80 >= 71:
    print("Your exam grade is a B")
elif exam_grade <= 100 >= 81:
    print("Your exam grade is a A")
